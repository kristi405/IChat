import UIKit

extension UIColor {
    
    static func redColor() -> UIColor {
        return #colorLiteral(red: 0.8156862745, green: 0.007843137255, blue: 0.1058823529, alpha: 1)
    }
    
    static func whiteColor() -> UIColor {
        return #colorLiteral(red: 0.9175784398, green: 0.9320262137, blue: 0.951022361, alpha: 1)
    }
    
    static func blackColor() -> UIColor {
        return #colorLiteral(red: 0.08746254281, green: 0.06431934932, blue: 0.09586365582, alpha: 1)
    }
    
    static func taxtFieldColor() -> UIColor {
        return #colorLiteral(red: 0.862745098, green: 0.862745098, blue: 0.862745098, alpha: 1)
    }
}


